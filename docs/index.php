        <h1 id="about2">Plugins</h1>
        <p>
            PieMC can be extended and customized with plugins.
            A plugin is an external module which can be “plugged in” to your server to add custom features, change
            default behaviours and more.
        </p>
        <h1 id="downloading_plugin">Downloading Plugins</h1>
        <p>
            You can find a wide range of premade plugins on <a href="/plugins">PieMC-Plugins</a> our release plugin
            platform.
        </p>
        <h1 id="installingorupdating">Installing/updating a plugin</h1>
        <p>
            Plugin developers usually distribute plugins as .zip files. These files can be easily loaded by placing them
            in your plugins folder in the server data folder.
            Be sure to delete old versions of the plugin, and restart the server to see changes.
        </p>

        <h1 id="about">Installation & Updating</h1>
        <p>
            A computer or device with the following is required:
            32-64-bit CPU
            32-64-bit operating system
            1GB RAM or better
        </p>
        <h3>Installing/updating manually</h3>
        <b>* Windows:</b>
        <p>
            Make sure to have Python installed as it is required to run PieMC!
            Make a folder for your server. Download the latest stable version of PieMC on github.
            Double click on start.cmd
        </p>
        <br>
        <b>* Linux:</b>
        <p>
            Make sure to have Python installed as it is required to run PieMC!
            Make a folder for your server. Download the latest stable version of PieMC on github.
            Run ./start.sh
        </p>
    </article>
